package atividade;

public class Disco {
    
    private int posicaoBraco;
    private int quantidadeSetores;

    public Disco() {
        
    }

    public int getPosicaoBraco() {
        return posicaoBraco;
    }

    public void setPosicaoBraco(int posicaoBraco) {
        this.posicaoBraco = posicaoBraco;
    }

    public int getQuantidadeSetores() {
        return quantidadeSetores;
    }

    public void setQuantidadeSetores(int quantidadeSetores) {
        this.quantidadeSetores = quantidadeSetores;
    }
    
}
